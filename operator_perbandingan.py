# > lebih dari
# < kurang dari
# >= lebih dari sama dengan
# <= kurang dari sama dengan
# == sama dengan
# != tidak sama dengan

print(1 < 4) #hasilnya true
print(1 > 4) #hasilnya false
print(1 >= 1) #hasilnya true
print(1 <= 1) #hasilnya true
print("Kwanzza" == "Kwanzza") #hasilnya true
print("Arsal" != "Arsal") #hasilnya false